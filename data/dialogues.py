    @classmethod
    def get_dialogue(cls, personality: PersonalityTrait, context: str) -> str:
        dialogues = cls.DIALOGUES.get(personality, {}).get(context, ["喵~"])
        return random.choice(dialogues)

    @classmethod
    def get_random_idle_line(cls, personality: PersonalityTrait) -> str:
        return cls.get_dialogue(personality, "idle")

    @classmethod
    def get_greeting(cls, personality: PersonalityTrait) -> str:
        return cls.get_dialogue(personality, "greeting")

    @classmethod
    def get_tip(cls, tips_key: str, name: str = "Pii") -> str:
        tips = getattr(cls, tips_key, [])
        return random.choice(tips).format(name=name)

    @classmethod
    def get_contextual_dialogue(cls, personality: PersonalityTrait, stats, name: str) -> str:
        """根据时间/状态/成长多维度选择互动文案"""
        from datetime import datetime
        from core.config import PetConfig

        hour = datetime.now().hour

        # 按优先级检查条件
        context = None
        if 22 <= hour or hour < 5:
            context = "late_night"
        elif stats.hunger <= PetConfig.PETTED_HUNGER_THRESHOLD:
            context = "hungry"
        elif stats.energy <= PetConfig.PETTED_ENERGY_THRESHOLD:
            context = "tired"
        elif stats.happiness >= PetConfig.HAPPINESS_HIGH_THRESHOLD:
            context = "happy"
        elif 6 <= hour < 9:
            context = "morning"
        elif 12 <= hour < 14:
            context = "afternoon"
        elif stats.age_days > 0 and stats.age_days % PetConfig.PETTED_MILESTONE_INTERVAL == 0:
            context = "milestone"

        # 命中条件时 50% 概率使用条件文案
        if context and random.random() < PetConfig.PETTED_CONTEXT_CHANCE:
            pool = cls.CONTEXT_DIALOGUES.get(personality, {}).get(context, [])
            if pool:
                text = random.choice(pool)
                return text.format(name=name, days=getattr(stats, "age_days", 0), level=getattr(stats, "level", 1))

        # 兜底：使用现有 petted 文案
        return cls.get_dialogue(personality, "petted")
